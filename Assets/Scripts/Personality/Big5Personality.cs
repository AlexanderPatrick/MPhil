using UnityEngine;
using System.Collections;

public class Big5Personality {
    public float openness;          // -1 = consistent/cautious , +1 = inventive/curious
    public float conscientiousness; // -1 = easy-going/careless , +1 = efficient/organized
    public float extraversion;      // -1 = solitary/reserved   , +1 = outgoing/energetic
    public float agreeableness;     // -1 = analytical/detached , +1 = friendly/compassionate
    public float neuroticism;       // -1 = secure/confident    , +1 = sensitive/nervous
}
